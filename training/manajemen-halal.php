                    <p align="justify">Manajemen Kehalalan merupakan standar sistem manajemen keamanan pangan global yang diaplikasikan untuk seluruh rantai pasokan makanan, dari mulai petani dan produsen ke pengolah dan pengepak, hingga transportasi dan penjualan. ISO 22000 adalah gabungan dari elemen utama ISO 9001 dan HACCP Plan. Industri pangan perlu memperhatikan keamanan pangan dan sangat disarankan untuk menerapkan Standar Manajemen Keamanan Pangan (SMKP )ISO 22000. Hal ini dilakukan untuk meningkatkan mutu produk yang dihasilkan, menambah kepercayaan diri perusahaan dan meningkatkan kepercayaan konsumen. ISO 22000 menggarisbawahi persyaratan untuk sistem manajemen keamanan pangan termasuk komunikasi interaktif, manajemen sistem, dan program-program pra-syarat. Standar ini berfokus pada pemastian rantai pasok, apakah prinsip-prinsip sistem manajemen telah diterapkan dan sesuai dengan prinsip-prinsip HACCP dari Codex Alimentarius.</p>
